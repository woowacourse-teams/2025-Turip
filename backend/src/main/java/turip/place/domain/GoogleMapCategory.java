    private static GoogleMapCategory ofEnglishCategoryName(String englishCategoryName) {
        return Arrays.stream(GoogleMapCategory.values())
                .filter(category -> category.englishCategoryName.equals(englishCategoryName))
                .findFirst()
                .orElse(NONE);
    }
